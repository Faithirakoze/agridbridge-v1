export function createTranslator(language) {
  const selectedLanguage = normalizeLanguage(language);
  const selectedStrings = STRINGS[selectedLanguage];
  const fallbackStrings = STRINGS.en;

  return (key, params = {}) => {
    const entry = selectedStrings[key] ?? fallbackStrings[key] ?? key;
    return typeof entry === 'function' ? entry(params) : entry;
  };
}

export function getLanguageOptionLabel(optionValue, language) {
  const selectedLanguage = normalizeLanguage(language);
  const labels = LANGUAGE_OPTION_LABELS[optionValue] || LANGUAGE_OPTION_LABELS.en;
  return labels[selectedLanguage] || labels.en;
}

export function getCropTypeLabel(value, language) {
  const selectedLanguage = normalizeLanguage(language);
  return CROP_TYPE_LABELS[selectedLanguage][value] || value.replace(/_/g, ' ');
}

export function getCropStatusLabel(value, language) {
  const selectedLanguage = normalizeLanguage(language);
  return CROP_STATUS_LABELS[selectedLanguage][value] || value.replace(/_/g, ' ');
}

export function getActivityTypeLabel(value, language) {
  const selectedLanguage = normalizeLanguage(language);
  return ACTIVITY_TYPE_LABELS[selectedLanguage][value] || value.replace(/_/g, ' ');
}

export function formatLocalizedDate(value, language, options) {
  return new Date(value).toLocaleDateString(
    normalizeLanguage(language) === 'rw' ? 'rw-RW' : 'en-RW',
    options
  );
}
